// 3개(=1주) 단위 디자인 변형 — A→B→C→A 회전
const VARIANT_BY_WEEK = ['A', 'B', 'C', 'A'];
const dayOffsets = [0, 2, 4]; // 월, 수, 금
function iso(weekIdx, dayIdx) {
  const base = Date.UTC(2026, 5, 29, 10, 0, 0); // 2026-06-29 19:00 KST
  return new Date(base + (weekIdx * 7 + dayOffsets[dayIdx]) * 86400000).toISOString();
}

const items = [];
WEEKS.forEach((wk, wi) =>
  wk.forEach((row, di) => {
    const [topic, kicker, headline, sub, dayLabel, motif, captionBody] = row;
    items.push({
      id: `w${wi + 1}-${['mon', 'wed', 'fri'][di]}`,
      topic,
      format: '카드뉴스',
      channels: ['instagram'],
      scheduledFor: iso(wi, di),
      score: 90 - (wi * 3 + di),
      status: 'planned',
      kicker,
      headline,
      sub,
      dayLabel,
      motif,
      variant: VARIANT_BY_WEEK[wi],
      palette: 'ink',
      captionBody,
      captionNote: sub,
      rationale: sub,
      slides: SLIDES[`w${wi + 1}-${['mon', 'wed', 'fri'][di]}`] || [],
    });
  }),
);

const plan = { updatedAt: new Date(Date.UTC(2026, 5, 25, 15, 0, 0)).toISOString(), items };
